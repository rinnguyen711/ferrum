/// PascalCase a snake_case api id (`blog_post` -> `BlogPost`). Same rule as
/// `openapi::schema::to_pascal`.
pub fn pascal(name: &str) -> String {
    name.split('_')
        .filter(|s| !s.is_empty())
        .map(|s| {
            let mut c = s.chars();
            match c.next() {
                Some(first) => first.to_uppercase().chain(c).collect::<String>(),
                None => String::new(),
            }
        })
        .collect()
}
